package com.ims.query.builder.client.exceptions;

/**
 * @author vpurdila
 *
 */
public class QueryBuilderClientException extends Exception
{
	public QueryBuilderClientException()
	{
		super();
	}

	public QueryBuilderClientException(String arg0)
	{
		super(arg0);
	}

	public QueryBuilderClientException(Throwable arg0)
	{
		super(arg0);
	}

	public QueryBuilderClientException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}
}
